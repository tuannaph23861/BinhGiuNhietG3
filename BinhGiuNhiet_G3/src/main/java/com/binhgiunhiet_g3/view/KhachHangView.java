    private void btnThemKhachHangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemKhachHangActionPerformed
        if (validateThem()) {
            this.khService.add(getKhachHang());
            MsgBox.alert(this, "Thêm thành công");
            fillTableKH();
        }

    }//GEN-LAST:event_btnThemKhachHangActionPerformed

    private void btnSuaKhachHangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaKhachHangActionPerformed
        int row = tblKhachHang.getSelectedRow();
        if (validateSua()) {
            KhachHang khachHang = listKH.get(row);

            khachHang.setTenKhachHang(txtTenKH.getText());
            khachHang.setNgaysinh(jdcNgaySinh.getDate());
            khachHang.setSdt(txtSDT.getText());
            khachHang.setDiachi(txtDiaChi.getText());
            khachHang.setEmail(txtEmail.getText());

            this.khService.update(khachHang);
            MsgBox.alert(this, "Sửa thành công");
            fillTableKH();

        }


    }//GEN-LAST:event_btnSuaKhachHangActionPerformed

