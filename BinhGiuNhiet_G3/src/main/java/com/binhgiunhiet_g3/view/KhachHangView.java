    private KhachHang getKhachHang() {
        KhachHang khachHang = new KhachHang();

        khachHang.setMa(txtMa.getText());
        khachHang.setTenKhachHang(txtTenKH.getText());
        khachHang.setNgaysinh(jdcNgaySinh.getDate());
        khachHang.setSdt(txtSDT.getText());
        khachHang.setDiachi(txtDiaChi.getText());
        khachHang.setEmail(txtEmail.getText());

        return khachHang;
    }

    private void fillTxt(KhachHang khachHang) {
        txtMa.setText(khachHang.getMa());
        txtTenKH.setText(khachHang.getTenKhachHang());
        jdcNgaySinh.setDate(khachHang.getNgaysinh());
        txtSDT.setText(khachHang.getSdt());
        txtDiaChi.setText(khachHang.getDiachi());
        txtEmail.setText(khachHang.getEmail());
    }

    private void fillTableKH(KhachHang kh) {
        this.dtm = (DefaultTableModel) tblKhachHang.getModel();
        dtm.setRowCount(0);
        dtm.addRow(new Object[]{
            kh.getMa(),
            kh.getTenKhachHang(),
            kh.getNgaysinh(),
            kh.getSdt(),
            kh.getDiachi(),
            kh.getEmail()
        });

    }
