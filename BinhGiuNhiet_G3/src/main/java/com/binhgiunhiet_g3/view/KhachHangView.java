    private void fillTableKH() {
        this.dtm = (DefaultTableModel) tblKhachHang.getModel();
        dtm.setRowCount(0);
        this.listKH = this.khService.getAll();
        for (KhachHang kh : listKH) {
            dtm.addRow(new Object[]{
                kh.getMa(),
                kh.getTenKhachHang(),
                kh.getNgaysinh(),
                kh.getSdt(),
                kh.getDiachi(),
                kh.getEmail()
            });
        }
    }

    private boolean validateThem() {
        if (txtMa.getText().trim().isEmpty() || txtTenKH.getText().trim().isEmpty() || txtEmail.getText().trim().isEmpty()
                || txtSDT.getText().trim().isEmpty() || jdcNgaySinh.getDate().equals("") || txtDiaChi.getText().trim().isEmpty()) {
            MsgBox.alert(this, "Thông tin không để trống");
            return false;
        }
        for (KhachHang kh : khService.getAll()) {
            if (txtMa.getText().trim().equals(kh.getMa())) {
                MsgBox.alert(this, "Khách hàng đã tồn tại");
                return false;
            }
        }
        return true;
    }

    private boolean validateSua() {
        if (txtMa.getText().trim().isEmpty() || txtTenKH.getText().trim().isEmpty() || txtEmail.getText().trim().isEmpty()
                || txtSDT.getText().trim().isEmpty() || jdcNgaySinh.getDate().equals("") || txtDiaChi.getText().trim().isEmpty()) {
            MsgBox.alert(this, "Thông tin không để trống");
            return false;
        }

        return true;
    }

