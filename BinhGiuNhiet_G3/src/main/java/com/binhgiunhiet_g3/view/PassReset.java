    private void btnResetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResetActionPerformed
        NhanVien nv = this.nv;
        String newPass = new String(txtNewPass.getPassword());
        String newPassVer = new String(txtNewPassVer.getPassword());
        if (newPassVer.equals(newPass)) {
            nv.setMatKhau(newPass);
            nvService.update(nv);
            MsgBox.alert(this, "Mật khẩu đã được làm mới");
        } else {
            MsgBox.alert(this, "Mật khẩu đã nhập không trùng nhau");
        }
    }//GEN-LAST:event_btnResetActionPerformed

    private void tgbHideActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tgbHideActionPerformed
        if (tgbHide.isSelected()) {
            txtNewPass.setEchoChar((char) 0);
        } else {
            txtNewPass.setEchoChar('*');
        }
    }//GEN-LAST:event_tgbHideActionPerformed

    private void tgbHideDownActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tgbHideDownActionPerformed
        if (tgbHideDown.isSelected()) {
            txtNewPassVer.setEchoChar((char) 0);
        } else {
            txtNewPassVer.setEchoChar('*');
        }
    }//GEN-LAST:event_tgbHideDownActionPerformed

